package com;

public class Ciclos6_PRSC {

	public static void main(String[] args) {
		/*
		 * 6.- Una persona desea invertir $1000.00 en un banco, el cual le otorga un 2% de interés 
		 * mensual. ¿Cuál será la cantidad de dinero que esta persona tendrá al cabo de un año si 
		 * todo el dinero lo reinvierte?
		 */

		double inv = 1000;
		double inte = .02;
		double total;
		double invn;
		
		for (int i=0; i<=12; i++ ) {
		total = inv*inte;
		invn = inv+total;
		inv = invn;
		
		
		}
		
		System.out.println("La cantidad total de dinero al finalizar el año es de: " +inv);
	}

}
